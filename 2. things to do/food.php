                Our Bangladeshi cuisine is a classic culinary art-form. Our food is ecstatic; a pure South Asian delicacy. The spectacular combination of our age old traditional values and a diverse range of perfectly blended spices give Bangladesh cuisines a unique taste for which people from all around the world do not hesitate to come back here again. Our foods and spices are so great that now these rule the whole world. Many of you might have already tasted those in abroad in many restaurants that provide Bangladeshi dishes.<br><br>

                Most of the Bangladeshi dishes are based on rice. Abundance of fishes in rivers and ponds and our love towards different varieties of fishes in many forms of dishes make the proverb “Fish and rice makes a Bengali” more meaningful. Specially Shorshe Ilish, a dish of smoked hilsha with mustard-seed paste, is considered as the most significant and important part of our Bangladeshi cuisine. Apart from that, we also have curry made out of freshly picked vegetables, different kinds of meat, lentils soup, etc in our dishes. And also, bhorta, mashed boiled vegetable such as potato, beans, papya, dal etc seasoned with onion, chili, mustard oil/ghee and other pungent spices, adds an extra emotional and obviously culinary appeal to our dishes.<br><br>

                Well, apart from the regular dishes mentioned previously, some dishes are specially designed to spice up our celebration and festivals. Biriyani or polao served with meat curries (chicken, beef, lamb etc.) seasoned with proper spices and ghee (butter) gives us and our celebration a heavenly smell and a satisfied tummy.<br><br>

                We are known for our passions for a wide variety of sweets made from milk. Rasho-gollah, kalo-jam, shandesh, mishti doi, shemai, chamcham… Quite frankly, the list is way too long. Come aboard sweet tooth!<br><br>

                Although pitha (cake) is popular for celebrating a specific season, but it has a far more reasons to be celebrated for which pitha is always prepared and cherished in any Bangladeshi dinner table all over the year. Most pithas are sweet but that does not stop us from making it spicy. There are some pithas which are made with meat and other spices. The chief ingredients of pitha are: sugarcane, date juice, rice, wheat flour, milk etc. Bhapa, pooli, patishapta, nakshipitha etc are common but celebrated in any Bangladeshi kitchen.<br><br>

                The deeper you roam around the alleys of any Bangladeshi town, the more exotic your culinary experience will become. Foods found in our streets and restaurants are as traditional as the foods cooked in our home. Iftar at Old Dhaka, jhalmuri (puffed rice with spices), dal puri, smokin’ hot Kebabs, etc. are surely never to be missed.<br><br>

                While you are trekking through the rough hills of Bandarban or Rangamati, make sure your try the traditional foods of your tribal people. These foods are their heritage and the outcome of their diverse culture.<br><br>

                You will be amazed to know that when you are visiting each part of Bangladesh, there exist many different local foods which are famous for their individual characteristics. Some go for spice or some go for sweets. The taste is obviously different than the mainstream ones but deep down it only depicts a unique theme; it is Bangladeshi food.
