            <p>
                The national Flag of Bangladesh (বাংলাদেশের জাতীয় পতাকা), known as Red-Green, was adopted officially on 17 January 1972. It consists of a red disc on top of a green field, offset slightly toward the hoist so that it appears centred when the flag is flying. The red disc represents the sun rising over Bengal, and also the blood of those who died for the independence of Bangladesh. The green field stands for the lushness of the land of Bangladesh.[1] The flag is based on a similar flag used during the Bangladesh Liberation War of 1971, which had a yellow map of the country inside the red disc. In 1972 this map was deleted from the flag. One reason given was the difficulty for rendering the map correctly on both sides of the flag. The civil ensign and naval ensign place the national flag in the canton of a red or white field, respectively.<br><br>

                With a red circle — which represents the rising sun and the bloody sacrifices of countless Bangladeshi people for their freedom — on a bottle green background which symbolizes our wonderful natural resources, the national flag of Bangladesh stands as high as our pride and love for our motherland.
                <br><br>

                The sanctity and the dignity of the National Flag is protected and officially preserved by an act of Bangladesh Flag rules, 1972. As per the rules, the national flag is rectangular in size with the length to width ratio of 10:6. Prescribed sizes of the flag for buildings are 305cm X 183cm, 152cm X 91cm and 76cm X 46cm and for vehicles are 38cm X 23cm and 25cm X 15cm. It was designed by famous artist Kamrul Hasan.
            </p><br>

